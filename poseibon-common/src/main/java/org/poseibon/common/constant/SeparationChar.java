package org.poseibon.common.constant;

/**
 * 分隔符常量类
 *
 * @author qingchuan
 * @date 2020/12/10
 */
public interface SeparationChar {
    /**
     * 冒号
     */
    char COLON = ':';
    /**
     * 分号
     */
    char SEMICOLON = ';';
    /**
     * 逗号
     */
    char COMMA = ',';
    /**
     * 顿号
     */
    char CAESURA = '、';
    /**
     * 大于号
     */
    char GT = '>';
    /**
     * 制表符
     */
    char TAG = '\t';
    /**
     * 换行符
     */
    String LINEFEED = "\r\n";
    /**
     * 斜杠
     */
    char SLASH = '/';
    /**
     * 下划线
     */
    char UNDERLINE = '_';
    /**
     * 点
     */
    char DOT = '.';
}
